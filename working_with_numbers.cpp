#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    int num1 = 5 ;
    int num2 = 3;

    cout << "The sum of the two numbers are " << num1 + num2 << endl;
    cout << "Moreover, " << num1 << " mod " << num2 << " is " << num1 % num2 << endl;
    cout << num1 << " raised to the power of " << num2 << " gives " << pow(num1,num2) << endl;
    cout << "Square root of " << num1 << " is " << sqrt(num1) << endl;

    return 0;
}

